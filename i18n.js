const languageMeta = {
  de: { code: "DE", flag: "flag-de", htmlLang: "de-CH" },
  en: { code: "EN", flag: "flag-gb", htmlLang: "en" },
  fr: { code: "FR", flag: "flag-fr", htmlLang: "fr" },
  it: { code: "IT", flag: "flag-it", htmlLang: "it" },
};

const trigger = document.querySelector(".language-trigger");
const menu = document.querySelector(".language-menu");
const currentCode = document.querySelector(".language-current-code");
const currentFlag = document.querySelector(".language-current-flag");
const options = document.querySelectorAll(".language-menu-option");

function applyLanguage(lang) {
  const dictionary = translations[lang] || translations.de;
  document.documentElement.lang = languageMeta[lang]?.htmlLang || "de-CH";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      const attribute = element.dataset.i18nAttr;
      if (attribute) {
        element.setAttribute(attribute, dictionary[key]);
      } else {
        element.textContent = dictionary[key];
      }
    }
  });

  const titleKey = document.documentElement.dataset.pageTitle;
  if (titleKey && dictionary[titleKey]) {
    document.title = dictionary[titleKey];
  }

  const meta = languageMeta[lang] || languageMeta.de;
  if (currentCode) {
    currentCode.textContent = meta.code;
  }
  if (currentFlag) {
    currentFlag.className = `language-current-flag flag ${meta.flag}`;
  }

  options.forEach((option) => {
    const isActive = option.dataset.lang === lang;
    option.classList.toggle("active", isActive);
    option.setAttribute("aria-checked", String(isActive));
  });

  try {
    localStorage.setItem("wittmer-appcare-language-v2", lang);
  } catch {
    // Ignore storage restrictions and keep the in-page language switch working.
  }
}

window.applyLanguage = applyLanguage;

function closeMenu() {
  menu.hidden = true;
  trigger.setAttribute("aria-expanded", "false");
}

if (trigger && menu) {
  let savedLanguage = "de";
  try {
    savedLanguage = localStorage.getItem("wittmer-appcare-language-v2") || "de";
  } catch {
    savedLanguage = "de";
  }
  applyLanguage(savedLanguage);

  trigger.addEventListener("click", () => {
    const isOpen = trigger.getAttribute("aria-expanded") === "true";
    menu.hidden = isOpen;
    trigger.setAttribute("aria-expanded", String(!isOpen));
  });

  options.forEach((option) => {
    option.addEventListener("click", () => {
      applyLanguage(option.dataset.lang);
      closeMenu();
    });
  });

  document.addEventListener("click", (event) => {
    if (!event.target.closest(".language-switcher")) {
      closeMenu();
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      closeMenu();
    }
  });
}

